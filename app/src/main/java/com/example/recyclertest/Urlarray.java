package com.example.recyclertest;

public class Urlarray {

    public int id;
    public String name;

    public Urlarray(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }



    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
